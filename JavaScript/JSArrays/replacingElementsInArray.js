/**
 * We can also replace an element or elements from an array from specified position(index) and a value can also added
 * to the end of the array.
 */
let studentName = ["Mrityunjay Kumar","Mrinal Sauraj","Vicky Raj","Sushma Swaraj"];
console.log(studentName);

studentName[2] = "Pratik";
console.log(studentName); 

studentName[0] = "Jyothi";
console.log(studentName); 

//adding a value to the end of the array.
studentName[4] = "princiee Gupta";
console.log(studentName); 
