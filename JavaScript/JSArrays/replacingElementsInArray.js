/**
 * We can also replace an element or elements from an array from specified position(index).
 */
let studentName = ["Mrityunjay Kumar","Mrinal Sauraj","Vicky Raj","Sushma Swaraj"];
console.log(studentName);

studentName[2] = "Pratik";
console.log(studentName); 

studentName[0] = "Jyothi";
console.log(studentName); 
